package com.microservices.msone.model;
public abstract class Entitys<T> {
    protected T id;
    protected String name;

    public Entitys(T id, String name) {
        this.id = id;
        this.name = name;
    }

    public T getId() {
        return id;
    }

    public void setId(T id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}